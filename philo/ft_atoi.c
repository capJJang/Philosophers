int	ft_atoi(char *s)
{
	int	ret;
	int	sign;

	sign = 1;
	ret = 0;
	if (!s)
		return (0);
	while (*s == 32 || (*s >= 9 && *s <= 13))
		s++;
	if (*s == '-')
	{
		sign *= -1;
		s++;
	}
	else if (*s == '+')
		s++;
	while (*s)
		ret = ret * 10 + *s++ - '0';
	return (sign * ret);
}
